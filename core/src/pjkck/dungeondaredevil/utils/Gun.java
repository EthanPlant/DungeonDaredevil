package pjkck.dungeondaredevil.utils;

public class Gun {
    // Gun attributes
    private float bulletSpeed;
    private float attackSpeed;
    private float range;
    private float spray;

    public float getBulletSpeed() {
        return bulletSpeed;
    }

    public float getAttackSpeed() {
        return attackSpeed;
    }

    public float getRange() {
        return range;
    }

    public float getSpray() {
        return spray;
    }
}
